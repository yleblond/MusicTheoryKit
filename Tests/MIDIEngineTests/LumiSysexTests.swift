import XCTest
@testable import MIDIEngine

// Mirrors Sources/SanityChecks/main.swift's "LumiSysexTests" section — see that section's
// header comment for how these expected byte arrays were derived (a Node run of the actual
// reverse-engineered lumi_sysex.js, cross-checked against SYSEX.txt's captured examples) and
// LumiSysex.envelope's own doc comment for why device-id 0x34 replaced the original 0x37.
final class LumiSysexTests: XCTestCase {

    func testSetColorAllKeysRed() {
        XCTAssertEqual(
            LumiSysex.setColor(.allKeys, red: 255, green: 0, blue: 0),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x20, 0x04, 0x00, 0x00, 0x7F, 0x7F, 0x03, 0x1B, 0xF7]
        )
    }

    func testSetColorAllKeysGreen() {
        XCTAssertEqual(
            LumiSysex.setColor(.allKeys, red: 0, green: 255, blue: 0),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x20, 0x04, 0x40, 0x7F, 0x00, 0x7E, 0x03, 0x46, 0xF7]
        )
    }

    func testSetColorAllKeysBlue() {
        XCTAssertEqual(
            LumiSysex.setColor(.allKeys, red: 0, green: 0, blue: 255),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x20, 0x64, 0x3F, 0x00, 0x00, 0x7E, 0x03, 0x30, 0xF7]
        )
    }

    func testSetColorRootArbitraryRGB() {
        XCTAssertEqual(
            LumiSysex.setColor(.root, red: 128, green: 64, blue: 32),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x04, 0x08, 0x20, 0x00, 0x7F, 0x03, 0x1C, 0xF7]
        )
    }

    func testSetColorAllKeysArbitraryRGB() {
        XCTAssertEqual(
            LumiSysex.setColor(.allKeys, red: 17, green: 200, blue: 99),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x20, 0x64, 0x18, 0x64, 0x11, 0x7E, 0x03, 0x7E, 0xF7]
        )
    }

    func testSetBrightnessZeroPercent() {
        XCTAssertEqual(
            LumiSysex.setBrightness(0),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x40, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x44, 0xF7]
        )
    }

    func testSetBrightnessFiftyPercent() {
        XCTAssertEqual(
            LumiSysex.setBrightness(50),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x40, 0x44, 0x0C, 0x00, 0x00, 0x00, 0x00, 0x50, 0xF7]
        )
    }

    func testSetBrightnessHundredPercent() {
        XCTAssertEqual(
            LumiSysex.setBrightness(100),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x40, 0x04, 0x19, 0x00, 0x00, 0x00, 0x00, 0x2D, 0xF7]
        )
    }

    func testSetColorModeAllFiveVariants() {
        // Expected bytes: live MIDI Monitor capture of ROLI Dashboard's five mode menu
        // entries against real hardware — see LumiSysex.ColorMode's doc comment.
        XCTAssertEqual(
            LumiSysex.setColorMode(.user),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x40, 0x2C, 0x00, 0x00, 0x00, 0x00, 0x00, 0x3C, 0xF7]
        )
        XCTAssertEqual(
            LumiSysex.setColorMode(.pro),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x40, 0x0C, 0x00, 0x00, 0x00, 0x00, 0x00, 0x5C, 0xF7]
        )
        XCTAssertEqual(
            LumiSysex.setColorMode(.stage),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x40, 0x6C, 0x00, 0x00, 0x00, 0x00, 0x00, 0x7C, 0xF7]
        )
        XCTAssertEqual(
            LumiSysex.setColorMode(.piano),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x40, 0x4C, 0x00, 0x00, 0x00, 0x00, 0x00, 0x1C, 0xF7]
        )
        XCTAssertEqual(
            LumiSysex.setColorMode(.rainbow),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x40, 0x0C, 0x01, 0x00, 0x00, 0x00, 0x00, 0x2D, 0xF7]
        )
    }

    func testSetScaleSamples() {
        // .blues and .lydian confirmed byte-for-byte via live Dashboard capture
        // (2026-07-20); the rest are SYSEX.txt's table, trusted on that same strength.
        XCTAssertEqual(
            LumiSysex.setScale(.major),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x60, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x7E, 0xF7]
        )
        XCTAssertEqual(
            LumiSysex.setScale(.dorian),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x60, 0x62, 0x01, 0x00, 0x00, 0x00, 0x00, 0x6F, 0xF7]
        )
        XCTAssertEqual(
            LumiSysex.setScale(.blues),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x60, 0x42, 0x01, 0x00, 0x00, 0x00, 0x00, 0x0F, 0xF7]
        )
        XCTAssertEqual(
            LumiSysex.setScale(.lydian),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x60, 0x22, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0xF7]
        )
        XCTAssertEqual(
            LumiSysex.setScale(.chromatic),
            [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x60, 0x42, 0x04, 0x00, 0x00, 0x00, 0x00, 0x02, 0xF7]
        )
    }

    func testSetKeyAllTwelvePitchClasses() {
        // C, C#, D, D#, A, A#, B confirmed byte-for-byte via live Dashboard capture
        // (2026-07-20); E, F, F#, G, G# are derived from the same BitPacker fed the
        // pattern that fit all 7 confirmed notes — see LumiSysex.setKey's doc comment.
        let expected: [Int: [UInt8]] = [
            0: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x03, 0x00, 0x00, 0x00, 0x00, 0x00, 0x41, 0xF7],
            1: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x23, 0x00, 0x00, 0x00, 0x00, 0x00, 0x21, 0xF7],
            2: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x43, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0xF7],
            3: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x63, 0x00, 0x00, 0x00, 0x00, 0x00, 0x61, 0xF7],
            4: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x03, 0x01, 0x00, 0x00, 0x00, 0x00, 0x12, 0xF7],
            5: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x23, 0x01, 0x00, 0x00, 0x00, 0x00, 0x72, 0xF7],
            6: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x43, 0x01, 0x00, 0x00, 0x00, 0x00, 0x52, 0xF7],
            7: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x63, 0x01, 0x00, 0x00, 0x00, 0x00, 0x32, 0xF7],
            8: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x03, 0x02, 0x00, 0x00, 0x00, 0x00, 0x63, 0xF7],
            9: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x23, 0x02, 0x00, 0x00, 0x00, 0x00, 0x43, 0xF7],
            10: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x43, 0x02, 0x00, 0x00, 0x00, 0x00, 0x23, 0xF7],
            11: [0xF0, 0x00, 0x21, 0x10, 0x77, 0x34, 0x10, 0x30, 0x63, 0x02, 0x00, 0x00, 0x00, 0x00, 0x03, 0xF7],
        ]
        for (pitchClass, bytes) in expected {
            XCTAssertEqual(LumiSysex.setKey(pitchClass: pitchClass), bytes)
        }
    }
}
